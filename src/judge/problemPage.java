    public void set()
    {
        conn = MySqlConnect.connection();
        String sql = "Select * from problems where number="+index;
        try
        {   
            //pst.setString(1, "ADD");
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next())
            {
                String name = rs.getString("problem");
                String statement = rs.getString("statement");
                int timelimit = rs.getInt("timelimit");
                String tags = rs.getString("tags");
                points = rs.getInt("points");
                jLabel1.setText(name);
                jTextField1.setText(statement);
                jTextField2.setText(""+timelimit);
                jTextField3.setText(""+points);
                jLabel4.setText(tags);
            }
        }
        catch(SQLException e)
        {
            System.out.println("Failed to fetch Problem");
        }
    }
