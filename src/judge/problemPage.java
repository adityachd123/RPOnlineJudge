    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String tag = "# " + jTextField4.getText();
        try
        {
                   
        String sql = "Select * from problems where number='"+index+"' ";
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();
        String temp = "";
        while(rs.next())
        {
            temp = rs.getString("tags");
        }
        if( temp == null )
            temp = tag ;
        else
            temp += (" " + tag );
        String sql1 = "Update data1.problems set tags = '"+temp+"' where number ='"+index+"'";
        conn.createStatement().execute(sql1);
        jLabel4.setText(jLabel4.getText()+tag);
        }
        catch(SQLException e)
        {
            System.out.println("Error while adding tag!!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

